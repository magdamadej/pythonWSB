print("Oblicz pole kwadrau")
bok_kwadratu = float(input("Podaj długość boku kwadratu: "))
if bok_kwadratu > 0:
    print(f"Pole kwadratu o boku {bok_kwadratu} wynosi: {bok_kwadratu ** 2}")

print("Oblicz pole prostokąta")
bok_1 = float(input("Podaj długość pierwszego boku: "))
bok_2 = float(input("Podaj długość drugiego boku: "))
if bok_1 > 0 and bok_2 > 0:
    print(f"Pole prostokąta o bokach {bok_1} i {bok_2} wynosi: {bok_1 * bok_2}")

